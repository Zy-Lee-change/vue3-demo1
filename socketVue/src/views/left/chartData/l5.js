const randomNum = (min, max) => {
  return Math.random() * (max - min) + min;
};

const getA = (x, y, id, category, len) => {
  let res_ = [];
  for (let i = 0; i < len; i++) {
    let ids = (id + i).toString();
    res_.push({
      id: ids,
      name: "MmeBurgon" + i,
      symbolSize: 10,
      x: randomNum(x[0], x[1]),
      y: randomNum(y[0], y[1]),
      value: 6.742859,
      category: category,
    });
  }
  return res_;
};

const getL = (id) => {
  let res_ = [];
  for (let i = id[1]; i > id[0]; i--) {
    let ind_ = parseInt(randomNum(1, 5));
    for (let j = 0; j < ind_; j++) {
      res_.push({
        source: i.toString(),
        target: parseInt(randomNum(id[0], i)).toString(),
      });
    }
  }
  return res_;
};

